        $(document).ready(function() {
            const $input = $('#search-input');
            const $suggestBox = $('#suggestions-box');
            const $list = $('#suggestion-list');
            let delayTimer;

            // Typing event
            $input.on('keyup', function() {
                clearTimeout(delayTimer);
                const query = $(this).val().trim();

                if (query.length < 2) {
                    $suggestBox.hide();
                    return;
                }

                delayTimer = setTimeout(function() {
                    $.ajax({
                        url: "{{ route('frontend.ajax-search') }}",
                        type: 'GET',
                        data: { q: query },
                        success: function(data) {
                            $list.empty();
                            if (data.length > 0) {
                                data.forEach(item => {
                                    let extra = '';
                                    if (item.type === 'product' && item.price) {
                                        extra = `<div class="price text-muted">${item.price} ৳</div>`;
                                    } else if (item.type === 'category') {
                                        extra = `<span class="badge bg-info text-white">Category</span>`;
                                    } else if (item.type === 'brand') {
                                        extra = `<span class="badge bg-success text-white">Brand</span>`;
                                    }

                                    $list.append(`
                                        <li>
                                            <a class="search-result-item d-flex align-items-center suggestion-item" 
                                            href="${item.url}">
                                                <div class="img-box me-2" style="width:45px;height:45px;">
                                                    <img src="${item.image}" alt="${item.name}" style="width:100%;height:100%;object-fit:cover;">
                                                </div>
                                                <div class="box-content">
                                                    <p class="title text-capitalize link mb-0">${item.name}</p>
                                                    ${extra}
                                                </div>
                                            </a>
                                        </li>
                                    `);
                                });
                                $suggestBox.show();
                            } else {
                                $list.html('<li class="px-3 py-2 text-muted">No results found</li>');
                                $suggestBox.show();
                            }
                        }
                    });
                }, 300);
            });

            $(document).on('click', '.suggestion-item', function() {
                $suggestBox.hide();
            });

            $(document).on('click', function(e) {
                if (!$(e.target).closest('.tf-form-search').length) {
                    $suggestBox.hide();
                }
            });

            $('#clear-search').on('click', function() {
                $input.val('');
                $suggestBox.hide();
            });

        });
    </script>




