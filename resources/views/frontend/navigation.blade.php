    $(document).on('click', '.add-to-cart', function (e) {
    e.preventDefault();

    const productId = $(this).data('id');
    const modalBody = $('#shopping-cart-content');

    // Check if user is logged in
    if (typeof isLoggedIn !== 'undefined' && !isLoggedIn) {
        $('#login').modal('show');
        return;
    }

    modalBody.html(`
        <div class="p-4 text-center">
            <div class="spinner-border text-primary" role="status">
                <span class="visually-hidden">Loading...</span>
            </div>
        </div>
    `);

    $.ajax({
        url: '/cart/add',
        type: 'POST',
        data: {
            _token: $('meta[name="csrf-token"]').attr('content'),
            product_id: productId
        },
        success: function (response) {
            if (response.success) {
                $.ajax({
                    url: '/cart/modal',
                    type: 'GET',
                    success: function (modalResponse) {
                        modalBody.html(modalResponse);
                        $('#shoppingCart').modal('show');
                    },
                    error: function () {
                        modalBody.html(`<div class="p-4 text-center text-danger">Failed to load cart.</div>`);
                    }
                });
            } else {
                modalBody.html(`<div class="p-4 text-center text-danger">${response.message}</div>`);
            }
        },
        error: function () {
            modalBody.html(`<div class="p-4 text-center text-danger">Something went wrong while adding to cart.</div>`);
        }
    });
});


